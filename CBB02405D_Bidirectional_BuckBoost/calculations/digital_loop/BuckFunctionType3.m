L=15E-6;
RL=2E-3;
C=440E-6;
RC=5E-3;
Vin=48;
Vout=40;
Iout= 5;
R=Vout/Iout;
w0=1/(sqrt(L*C));
wzc=1/RC/C;
Q=1/w0/(L/R+(RC+RL)*C);
Gdiv=3.3/68;
Gsample=1/3.3;
s=tf('s');
Gvd=Gdiv*Gsample*Vin*(1+s/wzc)/(1+s/Q/w0+s^2/w0^2)
wz1=3.142E3;
wz2=3.142E3;
wp1=3.142E5
wp2=3.142E5
ks=2.449E3
Gc=ks*(1+s/wz1)*(1+s/wz2)/s/(1+s/wp1)/(1+s/wp2)
sys=Gvd*Gc;
w=logspace(1,7);
p=bodeoptions;
p.PhaseVisible='on';
p.FreqUnits='Hz';
margin(sys);
grid on;
ts=5e-6;                     
dsys=c2d(Gc,ts,'tustin')    

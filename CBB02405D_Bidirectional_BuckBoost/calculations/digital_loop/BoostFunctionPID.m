L=15E-6;
RL=2E-3;
C=440E-6;
RC=5E-3;
Vin=24;
Vout=48;
Iout=5;
R=Vout/Iout;
Db=Vin/Vout;
w0=Db/(sqrt(L*C));
wa=Db^2*R/L;
wzc=1/RC/C;
Q=1/w0/(L/Db^2/R+(RC+RL/Db^2)*C);
Gdiv=3.3/68;
Gsample=1/3.3;
s=tf('s');
Gvd=Gdiv*Gsample*Vin*(1-s/wa)*(1+s/wzc)/Db^2/(1+s/Q/w0+s^2/w0^2)
wz1=3.142E3;
wz2=3.142E3;
ks=1.655E3;
Gc=ks*(1+s/wz1)*(1+s/wz2)/s
sys=Gvd*Gc;
w=logspace(1,7);
p=bodeoptions;
p.PhaseVisible='on';
p.FreqUnits='Hz';
margin(sys);
grid on;
ts=5e-6;                     
dsys=c2d(Gc,ts,'tustin')    
